    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Kontak::destroy($id);

        return redirect('/dashboard/contact')->with('destroy', 'Contact deleted successfully!');
    }
